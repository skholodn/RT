#include "uf_math.h"

inline t_vec3	mul_vec3(t_vec3 v1, t_vec3 v2)
{
	return (new_vec3(v1.y * v2.z - v1.z * v2.y,
					v1.z * v2.x - v1.x * v2.z,
					v1.x * v2.y - v1.y * v2.x));
}
